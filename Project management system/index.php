<?php
// index.php

include 'Task.php';
include 'Project.php';

// Пример использования системы управления проектами

$project = new Project('Разработка нового веб-сайта');

// Создание задач
$task1 = new Task('Дизайн', 'Создать макет главной страницы');
$task2 = new Task('Разработка', 'Разработать функционал для регистрации пользователей');

// Добавление задач в проект
$project->addTask($task1);
$project->addTask($task2);

// Изменение статуса задачи с помощью динамического вызова метода setStatus
$task1->setStatus('Выполнено');

// Вывод информации о проекте и задачах с использованием метода __toString()
echo $project . "\n";   // Вывод информации о проекте
echo $task1 . "\n";     // Вывод информации о первой задаче
echo $task2 . "\n";     // Вывод информации о второй задаче

// Удаление объектов (деструкторы будут вызваны автоматически)
unset($task1);
unset($task2);
unset($project);
?>